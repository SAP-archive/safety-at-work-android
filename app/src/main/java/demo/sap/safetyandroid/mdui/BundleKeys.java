package demo.sap.safetyandroid.mdui;

/*
 * Class to group all Bundle keys used between activities
 */
public final class BundleKeys {
    /*
     * The operation to be performed
     * Possible values are: OpRead, OpUpdate and UpCreate
     */
    public static final String OPERATION = "Operation";

    /*
     * The entity instance parameter to be displayed or updated
     */
    public static final String ENTITY_INSTANCE = "EntityInstance";
}
